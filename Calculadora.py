class Calculadora:
    def suma(self, a, b):
        resultado = int(a) + int(b)
        return resultado

    def resta(self, a, b):
        resultado = int(a) - int(b)
        return resultado

    def multiplicacion(self, a, b):
        resultado = int(a) * int(b)
        return resultado

    def division(self, a, b):
        resultado = int(a) / int(b)
        return resultado